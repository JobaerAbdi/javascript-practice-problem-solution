/* 
Question No: 10 => শর্টহ্যান্ড গুলো জানতে হবে। বিশেষ করে +=, -=, *=, /= জানতে হবে?
Answer : => 
( += ) this Addition assignment operator adds the value on its right to the variable or property on its left, and assigns the result to the variable or property on its left.
( -= ) this Subtraction assignment operator subtracts the value of the right operand from a variable and assigns the result to the variable.
( *= ) this Multiplication assignment operator first multiplies the value of the expression (on the right-hand side of the operator) by the value of the variable or property (on the left-hand side of the operator). 
( /= ) this Division assignment operator divides a variable by the value of the right operand and assigns the result to the variable. */



